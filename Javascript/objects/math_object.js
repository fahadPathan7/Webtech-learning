var val = Math.sqrt(16);
console.log(val); // 4

val = Math.abs(-5);
console.log(val); // 5

val = Math.pow(2, 3);
console.log(val); // 8

val = Math.random(); // from greater than 0 to less than 1
console.log(val);

val = Math.floor(2.4);
console.log(val); // 2

val = Math.ceil(2.4);
console.log(val); // 3

val = Math.max(2, 3, 1, 7); // unlimited
console.log(val); // 7

val = Math.min(2, 3, 1, 7); // unlimited
console.log(val); // 1

